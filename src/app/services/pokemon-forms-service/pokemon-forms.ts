export const formAliasById: Record<number, { baseId: number; form: PokemonForm }> =
    Object.entries(pokemonForms).reduce((aliases, [baseIdText, forms]) => {
        const baseId = Number(baseIdText);

        forms.forEach((form) => {
            if (form.pokemonId > 10000) {
                aliases[form.pokemonId] = {
                    baseId,
                    form,
                };
            }
        });

        return aliases;
    }, {} as Record<number, { baseId: number; form: PokemonForm }>);
